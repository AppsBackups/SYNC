const express = require('express');
const router = express.Router();
const multer = require('multer');
const path = require('path');
const companyImageController = require('../controllers/companyImageController');

// Multer storage
const storage = multer.diskStorage({
  destination: (req, file, cb) => cb(null, 'uploads/'),
  filename: (req, file, cb) => {
    const uniqueName = Date.now() + '-' + Math.round(Math.random() * 1e9) + path.extname(file.originalname);
    cb(null, uniqueName);
  }
});
const upload = multer({ storage });

// Upload image and get path
router.post('/image', upload.single('image'), companyImageController.uploadCompanyImage);

// Get image by global_id
router.get('/:global_id', companyImageController.getCompanyImage);

module.exports = router;
